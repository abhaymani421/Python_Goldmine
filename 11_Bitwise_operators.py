# Python Bitwise operators act on bits and perform bit-by-bit operations. 
# These are used to operate on binary numbers.

# Bitwise Operators in Python are as follows:

# Bitwise NOT
# Bitwise Shift
# Bitwise AND
# Bitwise XOR
# Bitwise OR
a = 10
b = 4

print(a & b)
print(a | b)
print(~a)
print(a ^ b)
print(a >> 2)
print(a << 2)