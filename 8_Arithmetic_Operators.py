# Variables
a = 15
b = 4

# Addition
print("Addition:", a + b)  

# Subtraction
print("Subtraction:", a - b) 

# Multiplication
print("Multiplication:", a * b)  

# Division
print("Division:", a / b) 

# Floor Division
print("Floor Division:", a // b)  

# Modulus
print("Modulus:", a % b) 

# Exponentiation
print("Exponentiation:", a ** b) # raises a to the power of b, a power b, a*a*a*a.....b times

#comparision between single division operators and double division(floor division) operators --->
print(5/2) # this prints the exact division
print(5//2) #this prints the floor of value of the integer division
